public class TypeConverwsion {
    public static void main(String[] args) {
        
        int i;
        double d =7.8;
        i = (int) d;
        System.out.println("d="+d);
        System.out.println("i="+i);
        i=9;
        d=(double)i;
        System.out.println("d="+d);
        System.out.println("i="+i);
    }
}






